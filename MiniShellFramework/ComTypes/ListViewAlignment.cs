namespace MiniShellFramework.ComTypes
{
    /// <summary>
    /// Defines options for alignment of the column header and the subitem text in the column.
    /// </summary>
    public enum ListViewAlignment
    {
        /// <summary>
        /// Text is left-aligned (LVCFMT_LEFT).
        /// </summary>
        Left = 0,

        /// <summary>
        /// Text is right-aligned (LVCFMT_RIGHT).
        /// </summary>
        Right = 1,

        /// <summary>
        /// Text is center-aligned (LVCFMT_CENTER).
        /// </summary>
        Center = 2
    }
}
